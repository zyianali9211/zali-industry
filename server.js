// Auth Middleware
function isAuthenticated(req, res, next) {
  if (req.session.adminId) {
    return next();
  }
  if (req.path.startsWith('/api/')) {
    return res.status(401).json({ error: 'Unauthorized. Please refresh and log in again.' });
  }
  res.redirect('/login');
}

