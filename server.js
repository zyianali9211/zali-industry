// STATIC SITE GENERATOR
// ========================
async function regenerateCatalog() {
  return new Promise((resolve, reject) => {
    console.log('Regenerating products-data.js...');
    
    db.all('SELECT * FROM categories ORDER BY sort_order ASC, id ASC', [], (err, categories) => {
      if (err) return reject(err);
      
      db.all('SELECT * FROM subcategories ORDER BY sort_order ASC, id ASC', [], (err, subcategories) => {
        if (err) return reject(err);
        
        db.all('SELECT * FROM products', [], (err, products) => {
          if (err) return reject(err);
          
          db.all('SELECT * FROM fabrics', [], (err, fabrics) => {
            if (err) return reject(err);

            const catalog = { categories: [] };
            
            categories.forEach(cat => {
              const catObj = {
                id: cat.id,
                name: cat.name,
                slug: cat.slug,
                subcategories: []
              };
              
              const catSubs = subcategories.filter(s => s.categoryId === cat.id);
              catSubs.forEach(sub => {
                const subObj = {
                  id: sub.id,
                  name: sub.name,
                  slug: sub.slug,
                  products: []
                };
                if (sub.group_name) {
                  subObj.group = sub.group_name;
                }
                
                const subProds = products.filter(p => p.subcategoryId === sub.id);
                subProds.forEach(prod => {
                  subObj.products.push({
                    id: prod.id,
                    name: prod.name,
                    folderName: prod.folderName,
                    category: cat.name,
                    categoryId: cat.id,
                    subcategory: sub.name,
                    subcategoryId: sub.id,
                    gender: prod.gender,
                    title: prod.title,
                    description: prod.description,
                    garmentType: prod.garmentType,
                    fabric: prod.fabric,
                    printMethods: prod.printMethods,
                    sizeRange: prod.sizeRange,
                    leadTime: prod.leadTime,
                    moq: prod.moq,
                    is_featured: prod.is_featured === 1,
                    primaryImageKey: prod.primary_image_key || null,
                    images: JSON.parse(prod.images || '{}')
                  });
                });
                catObj.subcategories.push(subObj);
              });
              catalog.categories.push(catObj);
            });
            
            const output = `/* ZALI Industries - Product Catalog Data */\n/* Auto-generated by Node.js Backend */\n\nwindow.ZALI_CATALOG = ${JSON.stringify(catalog, null, 2)};\n\n/* --- Flat lookup by product id --- */\nwindow.ZALI_PRODUCT_MAP = {};\nwindow.ZALI_CATALOG.categories.forEach(cat => {\n  cat.subcategories.forEach(sub => {\n    sub.products.forEach(p => {\n      window.ZALI_PRODUCT_MAP[p.id] = p;\n    });\n  });\n});\n`;
            fs.writeFileSync(path.join(__dirname, 'public', 'products-data.js'), output, 'utf8');

            const parsedFabrics = (fabrics || []).map(f => {
              let parsedImage = f.image;
              if (f.image && typeof f.image === 'string' && f.image.startsWith('[')) {
                try { parsedImage = JSON.parse(f.image); } catch (e) {}
              }
              return { ...f, image: parsedImage };
            });
            const fabricsOutput = `window.ZALI_FABRICS = ${JSON.stringify(parsedFabrics, null, 2)};\n`;
            fs.writeFileSync(path.join(__dirname, 'public', 'fabrics-data.js'), fabricsOutput, 'utf8');

            console.log('Successfully regenerated products-data.js and fabrics-data.js!');
            resolve();
          });
        });
      });
    });
  });
}

